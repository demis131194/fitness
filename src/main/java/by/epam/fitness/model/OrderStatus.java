package by.epam.fitness.model;

/**
 * The enum Order status.
 */
public enum OrderStatus {
    /**
     * New order status.
     */
    NEW,
    /**
     * Reviewed order status.
     */
    REVIEWED,
    /**
     * Rejected order status.
     */
    REJECTED,
    /**
     * Accepted order status.
     */
    ACCEPTED,
    /**
     * In processing order status.
     */
    IN_PROCESSING,
    /**
     * Terminated order status.
     */
    TERMINATED
}
